void main(){
    // operator aritmatika : untuk operasi matematika
    // + : pertambahan
    // * : perkalian
    // / : pembagian
    // - : pengurangan
    // % : sisa bagi / modulus
    // << dan >> : shift operator

    int a = 15;
    int b = 7;
    System.out.println("a + b = "+ (a + b));
    System.out.println("a - b = "+ (a - b));
    System.out.println("a * b = "+ (a * b));
    System.out.println("a / b = "+ (a / b)); // ini harus diperbaiki supaya hasilnya benar
    System.out.println("a % b = "+ (a % b));

    // perbaikan : lakukan type casting / type conversion pada tempat yang sesuai
}